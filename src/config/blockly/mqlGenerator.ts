import * as Blockly from 'blockly';

export const mqlGenerator = new Blockly.Generator('MQL') as any;

// Order of operation ENUMs
mqlGenerator.ORDER_ATOMIC = 0;           // 0 "" ...
mqlGenerator.ORDER_NEW = 1.1;            // new
mqlGenerator.ORDER_MEMBER = 1.2;         // . []
mqlGenerator.ORDER_FUNCTION_CALL = 2;    // ()
mqlGenerator.ORDER_INCREMENT = 3;        // ++
mqlGenerator.ORDER_DECREMENT = 3;        // --
mqlGenerator.ORDER_BITWISE_NOT = 4.1;    // ~
mqlGenerator.ORDER_UNARY_PLUS = 4.2;     // +
mqlGenerator.ORDER_UNARY_NEGATION = 4.3; // -
mqlGenerator.ORDER_LOGICAL_NOT = 4.4;    // !
mqlGenerator.ORDER_SIZEOF = 4.5;         // sizeof
mqlGenerator.ORDER_DIVISION = 5.1;       // /
mqlGenerator.ORDER_MULTIPLICATION = 5.2; // *
mqlGenerator.ORDER_MODULUS = 5.3;        // %
mqlGenerator.ORDER_SUBTRACTION = 6.1;    // -
mqlGenerator.ORDER_ADDITION = 6.2;       // +
mqlGenerator.ORDER_BITWISE_SHIFT = 7;    // << >>
mqlGenerator.ORDER_RELATIONAL = 8;       // < <= > >=
mqlGenerator.ORDER_EQUALITY = 9;         // == !=
mqlGenerator.ORDER_BITWISE_AND = 10;     // &
mqlGenerator.ORDER_BITWISE_XOR = 11;     // ^
mqlGenerator.ORDER_BITWISE_OR = 12;      // |
mqlGenerator.ORDER_LOGICAL_AND = 13;     // &&
mqlGenerator.ORDER_LOGICAL_OR = 14;      // ||
mqlGenerator.ORDER_CONDITIONAL = 15;     // ?:
mqlGenerator.ORDER_ASSIGNMENT = 16;      // = += -= *= /= %= <<= >>= &= ^= |=
mqlGenerator.ORDER_COMMA = 17;           // ,
mqlGenerator.ORDER_NONE = 99;            // (...)

// Initial setup
mqlGenerator.init = function (workspace: Blockly.Workspace) {
    // Create a list of defined variables
    const variables = Blockly.Variables.allUsedVarModels(workspace);
    const devars = [];
    for (let i = 0; i < variables.length; i++) {
        devars.push('double ' + mqlGenerator.variableDB_.getName(variables[i].getId(),
            Blockly.Names.NameType.VARIABLE) + ';');
    }
    mqlGenerator.definitions_['variables'] = devars.join('\n');
};

mqlGenerator.finish = function (code: string) {
    // Determine if workspace has actual logic
    const hasCode = code && code.trim().length > 0;

    // If workspace is empty, return simple message instead of boilerplate
    if (!hasCode) {
        return '// Add blocks to your workspace to generate MQL code';
    }

    // Convert global variables to MQL format
    const definitions = [];
    for (let name in mqlGenerator.definitions_) {
        definitions.push(mqlGenerator.definitions_[name]);
    }

    // Basic EA structure with generated code
    return `//+------------------------------------------------------------------+
//|                                             Generated Strategy |
//|                        Generated by PPM                        |
//+------------------------------------------------------------------+
#property copyright "Generated by PPM"
#property link      "https://ppm.example.com"
#property version   "1.00"
#property strict

${definitions.length > 0 ? definitions.join('\n') + '\n' : ''}
//+------------------------------------------------------------------+
//| Expert initialization function                                   |
//+------------------------------------------------------------------+
int OnInit()
  {
   return(INIT_SUCCEEDED);
  }
//+------------------------------------------------------------------+
//| Expert deinitialization function                                 |
//+------------------------------------------------------------------+
void OnDeinit(const int reason)
  {
  }
//+------------------------------------------------------------------+
//| Expert tick function                                             |
//+------------------------------------------------------------------+
void OnTick()
  {
${mqlGenerator.prefixLines(code, mqlGenerator.INDENT)}
  }
//+------------------------------------------------------------------+
`;
};

mqlGenerator.scrub_ = function (block: Blockly.Block, code: string, opt_thisOnly?: boolean) {
    const nextBlock = block.nextConnection && block.nextConnection.targetBlock();
    const nextCode = opt_thisOnly ? '' : mqlGenerator.blockToCode(nextBlock);
    return code + nextCode;
};

