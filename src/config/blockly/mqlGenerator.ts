// --- Control Blocks ---
mqlGenerator.forBlock['control_forever'] = function (block: Blockly.Block) {
    const doCode = mqlGenerator.statementToCode(block, 'DO');
    // In MQL, OnTick is the loop. We don't need while(true)
    return doCode;
};

mqlGenerator.forBlock['control_if'] = function (block: Blockly.Block) {
    const condition = mqlGenerator.valueToCode(block, 'CONDITION', mqlGenerator.ORDER_NONE) || 'false';
    const doCode = mqlGenerator.statementToCode(block, 'DO');
    return `if (${condition}) {\n${doCode}}\n`;
};

mqlGenerator.forBlock['control_if_else'] = function (block: Blockly.Block) {
    const condition = mqlGenerator.valueToCode(block, 'CONDITION', mqlGenerator.ORDER_NONE) || 'false';
    const doCode = mqlGenerator.statementToCode(block, 'DO');
    const elseCode = mqlGenerator.statementToCode(block, 'ELSE');
    return `if (${condition}) {\n${doCode}} else {\n${elseCode}}\n`;
};

mqlGenerator.forBlock['control_repeat'] = function (block: Blockly.Block) {
    const times = mqlGenerator.valueToCode(block, 'TIMES', mqlGenerator.ORDER_NONE) || '1';
    const doCode = mqlGenerator.statementToCode(block, 'DO');
    return `for (int i = 0; i < ${times}; i++) {\n${doCode}}\n`;
};

